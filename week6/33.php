echo "<h2>2. 求数列2/1,3/2,5/3,8/5,13/8,21/13,...的前15项之和，第15项的数是多少？（33.php）（试用函数来完成编程）</h2>";
//声明函数
function sum_of_series($n1)
{
    $a = 2;
    $b = 1;
    $s = 0;
    for ($n = 1; $n <= $n1; $n++) {
        $s = $s + $a / $b;
        $t = $a;
        $a = $a + $b;
        $b = $t;
    }
    return "数列的第{$n1}项为{$a}/{$b}<br>" . "数列2/1,3/2,5/3,8/5,13/8,21/13,...的前{$n1}项之和结果：{$s}";
};
// 使用函数
echo sum_of_series(15);
